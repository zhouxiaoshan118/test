package com.zte.zudp.modules.sys.previewUser.entity;

public class PreviewUserResources {
	private String id;
	private String previewUserID;
	private String resourceID;
	private String resourceStr;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPreviewUserID() {
		return previewUserID;
	}
	public void setPreviewUserID(String previewUserID) {
		this.previewUserID = previewUserID;
	}
	public String getResourceID() {
		return resourceID;
	}
	public void setResourceID(String resourceID) {
		this.resourceID = resourceID;
	}
	public String getResourceStr() {
		return resourceStr;
	}
	public void setResourceStr(String resourceStr) {
		this.resourceStr = resourceStr;
	}
}
